package org.example;


public class displayNumbers {
    public static int displayNumberPlusFive(int plus)
    {
        int numPlusFive;
        numPlusFive = plus + 5;
        return numPlusFive;

    }

    public static int displayNumberDoubled(int dub)
    {
        int numDoubled;
        numDoubled = dub * 2;
        return numDoubled;

    }

    public static int displayNumberSquared(int squared)
    {
       int numSquared;
       numSquared = (int) Math.pow(squared, 2);
       return  numSquared;
    }


}
